const ER = { timerInterval: null, started: false };

function getERState(state) {
  const ch4 = courseData.chapters.find(c => c.type === 'escape');
  const n = ch4?.puzzles?.length || 10;
  return state['c4-escape'] || {
    started: false, secondsLeft: 600, currentPuzzle: 0,
    solved: Array.from({ length: n }, () => []),   // per puzzle: array of solved area indices
    digits: Array(n).fill(null), completed: false, failed: false
  };
}

function saveERState(state, ers) {
  state['c4-escape'] = ers;
  saveProgress(state);
}

function formatERTime(s) {
  const m = Math.floor(s / 60);
  return `${m}:${String(s % 60).padStart(2, '0')}`;
}

function startERTimer() {
  if (ER.timerInterval) clearInterval(ER.timerInterval);
  ER.timerInterval = setInterval(() => {
    const state = loadProgress();
    const ers = getERState(state);
    if (ers.completed || ers.failed) { clearInterval(ER.timerInterval); return; }
    ers.secondsLeft = Math.max(0, (ers.secondsLeft ?? 600) - 1);
    saveERState(state, ers);
    const el = document.getElementById('er-timer');
    if (el) {
      el.textContent = formatERTime(ers.secondsLeft);
      el.classList.toggle('timer-urgent', ers.secondsLeft <= 60);
    }
    if (ers.secondsLeft <= 0) {
      clearInterval(ER.timerInterval); ER.timerInterval = null;
      ers.failed = true; saveERState(state, ers);
      showERGameOver(ers);
    }
  }, 1000);
}

function renderERCodeBoxes(digits, n) {
  return Array.from({ length: n }, (_, i) =>
    `<div class="er-code-box${digits[i] !== null ? ' filled' : ''}" id="er-box-${i}">${digits[i] !== null ? digits[i] : '·'}</div>`
  ).join('');
}

function renderERPuzzle(puzzle, solvedAreas) {
  const icons = ['א', 'ב', 'ג', 'ד', 'ה'];
  const areas = puzzle.areas.map((area, i) => {
    const done = solvedAreas.includes(i);
    let inner = '';
    if (area.type === 'mcq') {
      inner = `<div class="er-options">${area.options.map((opt, oi) =>
        `<button class="er-opt${done ? ' er-opt-disabled' : ''}" data-er-puzzle="${puzzle.id}" data-er-area="${i}" data-er-val="${oi}"${done ? ' disabled' : ''}>${opt}</button>`
      ).join('')}</div>`;
    } else { // tf
      inner = `<div class="er-tf-wrap">${['נכון ✓', 'לא נכון ✗'].map((label, ti) =>
        `<button class="er-tf${done ? ' er-opt-disabled' : ''}" data-er-puzzle="${puzzle.id}" data-er-area="${i}" data-er-val="${ti === 0}"${done ? ' disabled' : ''}>${label}</button>`
      ).join('')}</div>`;
    }
    return `<div class="er-area${done ? ' er-area-done' : ''}" id="er-area-${puzzle.id}-${i}">
      <div class="er-area-header">
        <span class="er-area-num">${icons[i]}</span>
        <span class="er-area-q">${area.q}</span>
        ${done ? '<span class="er-area-check">✓</span>' : ''}
      </div>
      ${done ? '' : inner}
    </div>`;
  }).join('');

  const doneCount = solvedAreas.length;
  const dots = puzzle.areas.map((_, i) =>
    `<div class="er-dot${solvedAreas.includes(i) ? ' done' : ''}"></div>`
  ).join('');

  return `
    <div class="er-puzzle" id="er-puzzle-${puzzle.id}">
      <div class="er-puzzle-header">
        <div class="er-puzzle-meta">
          <span class="er-puzzle-num">חידה ${puzzle.id}/10</span>
          <div class="er-puzzle-dots">${dots}</div>
          <span class="er-puzzle-count">${doneCount}/5 נפתחו</span>
        </div>
        <h3 class="er-puzzle-title">${puzzle.title}</h3>
        <p class="er-puzzle-story">${puzzle.story}</p>
      </div>
      <div class="er-areas">${areas}</div>
    </div>`;
}

