const BUBBLE_COLORS = [
  'bbl-violet', 'bbl-cyan', 'bbl-green',
  'bbl-amber', 'bbl-pink', 'bbl-blue',
  'bbl-teal', 'bbl-rose'
];

// Volatile per-session state
const G2 = {
  timerInterval: null,
  currentConceptId: null,
  feedbackActive: false,
  started: false,
  wrongStreak: 0
};

function getCh2State(state) {
  return state['c2-game'] || {
    order: null, currentIdx: 0,
    correctCount: 0, totalGuesses: 0,
    completed: false, secondsLeft: 210,
    started: false
  };
}

function saveCh2State(state, gs) {
  state['c2-game'] = gs;
  saveProgress(state);
}

function initCh2Order(state) {
  const ch2 = courseData.chapters.find(c => c.id === 2);
  if (!ch2) return getCh2State(state);
  const gs = getCh2State(state);
  if (!gs.order) {
    const ids = ch2.concepts.map(c => c.id);
    for (let i = ids.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [ids[i], ids[j]] = [ids[j], ids[i]];
    }
    gs.order = ids;
    saveCh2State(state, gs);
  }
  return gs;
}

function formatTime(s) {
  const m = Math.floor(s / 60);
  return `${m}:${String(s % 60).padStart(2, '0')}`;
}

function updateG2Displays(gs) {
  const goal = courseData.chapters.find(c => c.id === 2)?.goal || 18;
  const timerEl  = document.getElementById('g2-timer');
  const correctEl = document.getElementById('g2-correct');
  const guessEl  = document.getElementById('g2-guesses');
  const fillEl   = document.getElementById('g2-progress-fill');
  const numEl    = document.getElementById('g2-exp-num');

  if (timerEl) {
    timerEl.textContent = formatTime(G2.secondsLeft ?? gs.secondsLeft ?? 210);
    timerEl.classList.toggle('timer-urgent', (G2.secondsLeft ?? gs.secondsLeft ?? 210) <= 30);
  }
  if (correctEl) correctEl.textContent = `${gs.correctCount}/${goal}`;
  if (guessEl)   guessEl.textContent = gs.totalGuesses;
  if (fillEl)    fillEl.style.width = `${Math.min((gs.correctCount / goal) * 100, 100)}%`;
  if (numEl)     numEl.textContent = `${gs.correctCount + 1} / ${goal}`;
}

function startG2Timer(initialSeconds) {
  if (G2.timerInterval) clearInterval(G2.timerInterval);
  G2.secondsLeft = initialSeconds;

  G2.timerInterval = setInterval(() => {
    G2.secondsLeft--;
    const state = loadProgress();
    const gs = getCh2State(state);
    gs.secondsLeft = G2.secondsLeft;
    saveCh2State(state, gs);
    updateG2Displays(gs);

    if (G2.secondsLeft <= 0) {
      clearInterval(G2.timerInterval);
      G2.timerInterval = null;
      const gameEl = document.getElementById('matching-game-2');
      if (gameEl && !gs.completed && gs.correctCount < (courseData.chapters.find(c=>c.id===2)?.goal||18)) {
        const goal = courseData.chapters.find(c=>c.id===2)?.goal||18;
        const accuracy = gs.totalGuesses > 0 ? Math.round((gs.correctCount / gs.totalGuesses) * 100) : 0;
        gameEl.innerHTML = `
          <div class="game-over-card">
            <div class="game-over-icon">⏰</div>
            <h3>הזמן נגמר!</h3>
            <p>הצלחת לזהות <strong>${gs.correctCount}</strong> מתוך ${goal} מושגים.</p>
            <div class="game-final-stats">
              <div class="final-stat"><div class="final-stat-val">${gs.totalGuesses}</div><div class="final-stat-lbl">ניסיונות</div></div>
              <div class="final-stat"><div class="final-stat-val">${accuracy}%</div><div class="final-stat-lbl">דיוק</div></div>
            </div>
            <button class="btn-restart-game" data-chapter="2">↺ נסה שוב</button>
          </div>`;
      }
    }
  }, 1000);
}

function getDistractors(chapterId, excludeId, count) {
  const ch = courseData.chapters.find(c => c.id === chapterId);
  const pool = ch.concepts.filter(c => c.id !== excludeId).sort(() => Math.random() - 0.5);
  return pool.slice(0, count);
}

function renderBubblesDOM(correct, distractors) {
  const field = document.getElementById('g2-bubble-field');
  if (!field) return;
  const all = [correct, ...distractors].sort(() => Math.random() - 0.5);
  field.innerHTML = all.map((c, i) => {
    const color = BUBBLE_COLORS[i % BUBBLE_COLORS.length];
    const animN = (i % 6) + 1;
    const delay = (Math.random() * 2.5).toFixed(2);
    const dur   = (Math.random() * 2 + 3.5).toFixed(2);
    return `<button class="bubble ${color}" data-concept-id="${c.id}"
      style="animation-name:bob-${animN};animation-duration:${dur}s;animation-delay:-${delay}s;">
      ${c.term}
    </button>`;
  }).join('');
}

function showCurrentConcept(state) {
  const ch2 = courseData.chapters.find(c => c.id === 2);
  const goal = ch2?.goal || 18;
  const gs = getCh2State(state);

  if (gs.correctCount >= goal) { showG2Complete(gs); return; }

  const conceptId = gs.order[gs.currentIdx % gs.order.length];
  const concept   = ch2.concepts.find(c => c.id === conceptId);
  if (!concept) return;

  G2.currentConceptId = conceptId;
  G2.feedbackActive = false;

  const expText = document.getElementById('g2-exp-text');
  if (expText) expText.textContent = concept.explanation;

  renderBubblesDOM(concept, getDistractors(2, conceptId, 7));
  updateG2Displays(gs);
}

function showG2Complete(gs) {
  if (G2.timerInterval) { clearInterval(G2.timerInterval); G2.timerInterval = null; }
  const gameEl = document.getElementById('matching-game-2');
  if (!gameEl) return;
  const accuracy = gs.totalGuesses > 0 ? Math.round((gs.correctCount / gs.totalGuesses) * 100) : 100;
  const timeUsed = formatTime(210 - (gs.secondsLeft || 0));
  gameEl.innerHTML = `
    <div class="game-complete-card">
      <div class="game-complete-icon">🏆</div>
      <h3>פרק הושלם!</h3>
      <p>זיהית ${gs.correctCount} מושגים בהצלחה!</p>
      <div class="game-final-stats">
        <div class="final-stat"><div class="final-stat-val">${gs.totalGuesses}</div><div class="final-stat-lbl">ניסיונות</div></div>
        <div class="final-stat"><div class="final-stat-val">${accuracy}%</div><div class="final-stat-lbl">דיוק</div></div>
        <div class="final-stat"><div class="final-stat-val">${timeUsed}</div><div class="final-stat-lbl">זמן</div></div>
      </div>
    </div>`;

  const state = loadProgress();
  updateNavProgress(calcStats(state).pct);
  updateHeroStats(state);
}

function showG2Feedback(isCorrect, clickedTerm, correctTerm, hintMsg) {
  const zone = document.getElementById('g2-feedback-zone');
  if (!zone) return;
  zone.className = `g2-feedback-zone visible ${isCorrect ? 'g2-fb-ok' : 'g2-fb-no'}`;
  zone.innerHTML = isCorrect
    ? `<div class="g2-feedback-inline">✅ <strong>${clickedTerm}</strong> — נכון!</div>`
    : `<div class="g2-feedback-inline">❌ לא נכון — <strong>${clickedTerm}</strong></div>
       ${hintMsg ? `<div class="g2-fb-hint">${hintMsg}</div>` : ''}`;
}

function clearG2Feedback() {
  const zone = document.getElementById('g2-feedback-zone');
  if (zone) { zone.className = 'g2-feedback-zone'; zone.innerHTML = ''; }
}

function handleBubbleClick(conceptId, evt) {
  if (!G2.started) return;
  if (G2.feedbackActive) return;
  const ch2 = courseData.chapters.find(c => c.id === 2);
  const goal = ch2?.goal || 18;
  const state = loadProgress();
  const gs = getCh2State(state);

  gs.totalGuesses++;
  const isCorrect = conceptId === G2.currentConceptId;
  const correctConcept = ch2.concepts.find(c => c.id === G2.currentConceptId);
  const clickedConcept = ch2.concepts.find(c => c.id === conceptId);

  if (isCorrect) {
    gs.correctCount++;
    gs.currentIdx = (gs.currentIdx + 1) % ch2.concepts.length;
    G2.wrongStreak = 0;
  } else {
    G2.wrongStreak++;
  }
  saveCh2State(state, gs);

  const btn = document.querySelector(`.bubble[data-concept-id="${conceptId}"]`);
  if (btn) {
    G2.feedbackActive = true;
    btn.classList.add(isCorrect ? 'bbl-correct' : 'bbl-wrong');

    const skipAfterWrong = !isCorrect && G2.wrongStreak >= 3;
    const hintMsg = skipAfterWrong
      ? `המושג הנכון הוא: <strong>${correctConcept?.term}</strong> — עוברים למושג הבא`
      : (!isCorrect ? `ניסיון ${G2.wrongStreak}/3 — עוד ${3 - G2.wrongStreak} שגיאות ועוברים הלאה` : '');

    showG2Feedback(isCorrect, clickedConcept?.term || '', correctConcept?.term || '', hintMsg);

    if (isCorrect) {
      launchConfetti(btn);
      const delay = gs.correctCount >= goal ? 1400 : 900;
      setTimeout(() => {
        clearG2Feedback();
        btn.classList.remove('bbl-correct');
        G2.feedbackActive = false;
        const newState = loadProgress();
        const newGs = getCh2State(newState);
        if (newGs.correctCount >= goal) {
          newGs.completed = true;
          saveCh2State(newState, newGs);
          showG2Complete(newGs);
          launchConfetti(document.getElementById('matching-game-2'));
          updateNavProgress(calcStats(newState).pct);
          updateHeroStats(newState);
        } else {
          showCurrentConcept(newState);
        }
      }, delay);
    } else if (skipAfterWrong) {
      G2.wrongStreak = 0;
      gs.currentIdx = (gs.currentIdx + 1) % ch2.concepts.length;
      saveCh2State(state, gs);
      setTimeout(() => {
        clearG2Feedback();
        btn.classList.remove('bbl-wrong');
        G2.feedbackActive = false;
        const newState = loadProgress();
        showCurrentConcept(newState);
      }, 1800);
    } else {
      setTimeout(() => {
        clearG2Feedback();
        btn.classList.remove('bbl-wrong');
        G2.feedbackActive = false;
        updateG2Displays(gs);
      }, 1100);
    }
  }
  updateG2Displays(gs);
}

function resetG2Game() {
  if (G2.timerInterval) { clearInterval(G2.timerInterval); G2.timerInterval = null; }
  G2.started = false;
  G2.wrongStreak = 0;
  const state = loadProgress();
  state['c2-game'] = { order: null, currentIdx: 0, correctCount: 0, totalGuesses: 0, completed: false, secondsLeft: 210, started: false };
  saveProgress(state);
  renderApp();
  setTimeout(() => initG2Game(), 50);
}

