function loadProgress() {
  try {
    return JSON.parse(localStorage.getItem(STORAGE_KEY) || '{}');
  } catch {
    return {};
  }
}

function saveProgress(state) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
}

function getLessonKey(chapterId, lessonId) {
  return `c${chapterId}-l${lessonId}`;
}

function getLessonState(state, chapterId, lessonId) {
  const key = getLessonKey(chapterId, lessonId);
  return state[key] || { answers: [] };
}

function setAnswer(state, chapterId, lessonId, qIndex, value) {
  const key = getLessonKey(chapterId, lessonId);
  if (!state[key]) state[key] = { answers: [] };
  state[key].answers[qIndex] = value;
  saveProgress(state);
}

function resetLesson(state, chapterId, lessonId) {
  const key = getLessonKey(chapterId, lessonId);
  state[key] = { answers: [] };
  saveProgress(state);
}

function calcStats(state) {
  let totalQ = 0, answered = 0;
  courseData.chapters.forEach(ch => {
    if (ch.isBonus) return; // bonus chapters don't count toward progress
    if (ch.type === 'matching') {
      const goal = ch.goal || 18;
      totalQ += goal;
      const gs = getCh2State(state);
      answered += Math.min(gs.correctCount || 0, goal);
    } else {
      ch.lessons?.forEach(les => {
        totalQ += les.questions.length;
        const ls = getLessonState(state, ch.id, les.id);
        answered += ls.answers.filter(a => a !== undefined && a !== null).length;
      });
    }
  });
  return { totalQ, answered, pct: totalQ > 0 ? Math.round((answered / totalQ) * 100) : 0 };
}

function calcChapterStats(state, chapter) {
  let done = 0;
  chapter.lessons.forEach(les => {
    const ls = getLessonState(state, chapter.id, les.id);
    const answeredCount = ls.answers.filter(a => a !== undefined && a !== null).length;
    if (answeredCount === les.questions.length) done++;
  });
  return done;
}

function isLessonComplete(state, chapterId, lesson) {
  const ls = getLessonState(state, chapterId, lesson.id);
  return ls.answers.filter(a => a !== undefined && a !== null).length === lesson.questions.length;
}

function countLessonAnswered(state, chapterId, lesson) {
  const ls = getLessonState(state, chapterId, lesson.id);
  return ls.answers.filter(a => a !== undefined && a !== null).length;
}

