// ================================

function renderQuestion(state, chapterId, lessonId, qIndex, q) {
  const ls = getLessonState(state, chapterId, lessonId);
  const userAnswer = ls.answers[qIndex];
  const hasAnswer = userAnswer !== undefined && userAnswer !== null;
  const isCorrect = hasAnswer && (userAnswer === q.correct);

  const trueSelected = hasAnswer && userAnswer === true;
  const falseSelected = hasAnswer && userAnswer === false;

  let trueCls = 'btn-answer';
  let falseCls = 'btn-answer';

  if (hasAnswer) {
    if (trueSelected) trueCls += isCorrect && q.correct ? ' selected-correct' : (!q.correct ? ' selected-wrong' : ' selected-correct');
    if (falseSelected) falseCls += isCorrect && !q.correct ? ' selected-correct' : (q.correct ? ' selected-wrong' : ' selected-correct');
    // show what was correct
    if (hasAnswer && !isCorrect) {
      if (q.correct === true) trueCls += ' show-correct';
      if (q.correct === false) falseCls += ' show-correct';
    }
  }

  const feedbackHidden = hasAnswer ? '' : 'hidden';
  const feedbackType = isCorrect ? 'correct' : 'wrong';
  const feedbackIcon = isCorrect ? '✅' : '❌';
  const feedbackVerdict = isCorrect ? 'נכון!' : 'לא נכון';

  const questionCardCls = hasAnswer
    ? `question ${isCorrect ? 'answered-correct' : 'answered-wrong'}`
    : 'question';

  return `
    <div class="${questionCardCls}" data-chapter="${chapterId}" data-lesson="${lessonId}" data-qindex="${qIndex}">
      <div class="question-top">
        <span class="question-num">${String(qIndex + 1).padStart(2, '0')}</span>
        <p class="question-text">${q.text}</p>
      </div>
      <div class="answer-buttons">
        <button
          class="${trueCls}"
          data-chapter="${chapterId}" data-lesson="${lessonId}" data-qindex="${qIndex}" data-value="true"
          ${hasAnswer ? 'disabled' : ''}>
          <span class="btn-icon">✓</span> נכון
        </button>
        <button
          class="${falseCls}"
          data-chapter="${chapterId}" data-lesson="${lessonId}" data-qindex="${qIndex}" data-value="false"
          ${hasAnswer ? 'disabled' : ''}>
          <span class="btn-icon">✗</span> לא נכון
        </button>
      </div>
      <div class="feedback ${feedbackType} ${feedbackHidden}" id="feedback-${chapterId}-${lessonId}-${qIndex}">
        <span class="feedback-icon">${feedbackIcon}</span>
        <div class="feedback-body">
          <div class="feedback-verdict">${feedbackVerdict}</div>
          <div class="feedback-text">${makeFeedbackHTML(q.feedback)}</div>
        </div>
      </div>
    </div>`;
}

