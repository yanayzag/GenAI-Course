function initERGame() {
  const state = loadProgress();
  const ers = getERState(state);
  if (!ers.started || ers.completed || ers.failed) return;
  ER.started = true;
  startERTimer();
}

function startERGame() {
  const state = loadProgress();
  const ers = getERState(state);
  ers.started = true;
  saveERState(state, ers);
  ER.started = true;
  renderApp();
  setTimeout(startERTimer, 50);
}

function resetERGame() {
  if (ER.timerInterval) { clearInterval(ER.timerInterval); ER.timerInterval = null; }
  ER.started = false;
  const state = loadProgress();
  const ch4 = courseData.chapters.find(c => c.type === 'escape');
  const n = ch4?.puzzles?.length || 10;
  state['c4-escape'] = {
    started: false, secondsLeft: 600, currentPuzzle: 0,
    solved: Array.from({ length: n }, () => []),
    digits: Array(n).fill(null), completed: false, failed: false
  };
  saveProgress(state);
  renderApp();
}

function handleERClick(e) {
  // Start button
  if (e.target.closest('.btn-er-start')) { startERGame(); return; }
  // Restart button
  if (e.target.closest('.btn-er-restart')) { resetERGame(); return; }

  // Answer buttons (MCQ or TF)
  const btn = e.target.closest('[data-er-puzzle]');
  if (!btn || btn.disabled) return;

  const puzzleId = parseInt(btn.dataset.erPuzzle);
  const areaIdx  = parseInt(btn.dataset.erArea);
  const rawVal   = btn.dataset.erVal;

  const ch4 = courseData.chapters.find(c => c.type === 'escape');
  const puzzle = ch4?.puzzles?.find(p => p.id === puzzleId);
  if (!puzzle) return;

  const area = puzzle.areas[areaIdx];
  const state = loadProgress();
  const ers = getERState(state);

  // Already solved
  if ((ers.solved?.[puzzleId - 1] || []).includes(areaIdx)) return;

  // Check answer
  let isCorrect = false;
  if (area.type === 'mcq') {
    isCorrect = parseInt(rawVal) === area.correct;
  } else { // tf
    isCorrect = (rawVal === 'true') === area.correct;
  }

  if (isCorrect) {
    // Mark area solved
    if (!ers.solved) ers.solved = Array.from({ length: ch4.puzzles.length }, () => []);
    ers.solved[puzzleId - 1] = [...(ers.solved[puzzleId - 1] || []), areaIdx];
    saveERState(state, ers);

    // Update area UI: mark done
    const areaEl = document.getElementById(`er-area-${puzzleId}-${areaIdx}`);
    if (areaEl) {
      areaEl.classList.add('er-area-done');
      areaEl.querySelector('.er-area-header').insertAdjacentHTML('beforeend', '<span class="er-area-check">✓</span>');
      areaEl.querySelectorAll('.er-opt, .er-tf').forEach(b => { b.disabled = true; b.classList.add('er-opt-disabled'); });
      // Hide options row
      const opts = areaEl.querySelector('.er-options, .er-tf-wrap');
      if (opts) opts.style.display = 'none';
    }

    // Update dot
    const puzzleEl = document.getElementById(`er-puzzle-${puzzleId}`);
    const dots = puzzleEl?.querySelectorAll('.er-dot');
    if (dots) {
      const solvedAreas = ers.solved[puzzleId - 1];
      dots.forEach((d, i) => d.classList.toggle('done', solvedAreas.includes(i)));
    }

    // Update count
    const countEl = puzzleEl?.querySelector('.er-puzzle-count');
    if (countEl) countEl.textContent = `${ers.solved[puzzleId - 1].length}/5 נפתחו`;

    launchConfetti(btn);

    // Puzzle complete?
    if (ers.solved[puzzleId - 1].length === 5) {
      ers.digits[puzzleId - 1] = puzzle.digit;
      // Check if all done
      const allDone = ers.solved.every(s => s.length === 5);
      if (allDone) { ers.completed = true; }
      saveERState(state, ers);
      updateERCodeBox(puzzleId - 1, puzzle.digit);

      setTimeout(() => {
        showERDigitReveal(puzzle, () => {
          if (allDone) {
            showERSuccess(getERState(loadProgress()));
          } else {
            ers.currentPuzzle = puzzleId; // advance to next puzzle (0-indexed = puzzleId since id is 1-based)
            saveERState(loadProgress(), ers);
            const gameArea = document.getElementById('er-game-area');
            if (gameArea) {
              gameArea.innerHTML = renderERPuzzle(ch4.puzzles[puzzleId], []);
            }
          }
        });
      }, 300);
    }
  } else {
    // Wrong answer feedback
    btn.classList.add('er-opt-wrong');
    setTimeout(() => btn.classList.remove('er-opt-wrong'), 700);
  }
}

