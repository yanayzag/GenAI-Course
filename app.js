document.addEventListener('click', function(e) {
  // Theme toggle
  const themeBtn = e.target.closest('#btn-theme');
  if (themeBtn) { toggleTheme(); return; }

  // Chapter header toggle
  const chHeader = e.target.closest('[data-chapter-toggle]');
  if (chHeader) {
    const chId = parseInt(chHeader.dataset.chapterToggle);
    CHAPTER_OPEN[chId] = !CHAPTER_OPEN[chId];
    const content  = document.getElementById(`chapter-content-${chId}`);
    const chevron  = chHeader.querySelector('.chapter-chevron');
    if (content) content.classList.toggle('collapsed', !CHAPTER_OPEN[chId]);
    if (chevron) chevron.classList.toggle('open', CHAPTER_OPEN[chId]);
    return;
  }

  // Bubble click (chapter 2)
  const bubble = e.target.closest('.bubble');
  if (bubble && !bubble.disabled) {
    handleBubbleClick(bubble.dataset.conceptId, e);
    return;
  }

  // Start game button
  const startBtn = e.target.closest('.btn-start-game');
  if (startBtn) { startG2Game(); return; }

  // Restart game button
  const restartBtn = e.target.closest('.btn-restart-game');
  if (restartBtn) {
    resetG2Game();
    return;
  }

  // MC answer button (Chapter 3 – tool lessons)
  const mcBtn = e.target.closest('.btn-mc-answer');
  if (mcBtn && !mcBtn.disabled) {
    const chId  = parseInt(mcBtn.dataset.chapter);
    const lesId = parseInt(mcBtn.dataset.lesson);
    const selIdx = parseInt(mcBtn.dataset.index);

    const state = loadProgress();
    const prevAnswered = state[`c${chId}-l${lesId}`]?.answers?.[0] !== undefined;
    if (prevAnswered) return;

    setAnswer(state, chId, lesId, 0, selIdx);

    const chapter = courseData.chapters.find(c => c.id === chId);
    const lesson  = chapter?.lessons.find(l => l.id === lesId);
    const lessonIdx = chapter?.lessons.indexOf(lesson);
    if (!chapter || !lesson) return;

    const isCorrect = selIdx === lesson.questions[0].correctIndex;
    if (isCorrect) launchConfetti(mcBtn);

    updateLessonCard(state, chapter, lesson, lessonIdx);
    updateChapterDots(state, chapter);
    updateNavProgress(calcStats(state).pct);
    updateHeroStats(state);

    if (isCorrect) {
      setTimeout(() => launchConfetti(document.querySelector(`#lesson-${chId}-${lesId} .lesson-completion`)), 250);
    }
    showCompletionIfDone(state);
    return;
  }

  // Answer button
  const btn = e.target.closest('.btn-answer');
  if (btn && !btn.disabled) {
    const chId = parseInt(btn.dataset.chapter);
    const lesId = parseInt(btn.dataset.lesson);
    const qIdx = parseInt(btn.dataset.qindex);
    const value = btn.dataset.value === 'true';

    const state = loadProgress();
    // Check if this is a first-time correct answer before saving
    const question = courseData.chapters.find(c=>c.id===chId)?.lessons?.find(l=>l.id===lesId)?.questions?.[qIdx];
    const prevAnswered = state[`c${chId}-l${lesId}`]?.answers?.[qIdx] !== undefined;
    setAnswer(state, chId, lesId, qIdx, value);

    if (!prevAnswered && question && value === question.correct) {
      launchConfetti(btn);
    }

    // Find chapter + lesson
    const chapter = courseData.chapters.find(c => c.id === chId);
    const lesson = chapter?.lessons.find(l => l.id === lesId);
    const lessonIdx = chapter?.lessons.indexOf(lesson);
    if (!chapter || !lesson) return;

    // Re-render just this lesson card
    updateLessonCard(state, chapter, lesson, lessonIdx);
    updateChapterDots(state, chapter);
    updateNavProgress(calcStats(state).pct);
    updateHeroStats(state);

    // Confetti on lesson completion
    if (isLessonComplete(state, chId, lesson)) {
      setTimeout(() => launchConfetti(document.querySelector(`#lesson-${chId}-${lesId} .lesson-completion`)), 200);
    }

    showCompletionIfDone(state);
    return;
  }

  // Reset lesson button
  const resetBtn = e.target.closest('.btn-reset');
  if (resetBtn) {
    const chId = parseInt(resetBtn.dataset.chapter);
    const lesId = parseInt(resetBtn.dataset.lesson);

    const state = loadProgress();
    resetLesson(state, chId, lesId);

    const chapter = courseData.chapters.find(c => c.id === chId);
    const lesson = chapter?.lessons.find(l => l.id === lesId);
    const lessonIdx = chapter?.lessons.indexOf(lesson);
    if (!chapter || !lesson) return;

    updateLessonCard(state, chapter, lesson, lessonIdx);
    updateChapterDots(state, chapter);
    updateNavProgress(calcStats(state).pct);
    updateHeroStats(state);
    return;
  }

  // Escape Room (chapter 4)
  if (e.target.closest('#escape-room-4, .escape-room-wrap')) {
    handleERClick(e);
    return;
  }
});

