function initG2Game() {
  const state = loadProgress();
  const gs = initCh2Order(state);
  const goal = courseData.chapters.find(c => c.id === 2)?.goal || 18;

  if (gs.completed || gs.correctCount >= goal) { showG2Complete(gs); return; }

  G2.started = gs.started === true;
  G2.wrongStreak = 0;

  if (G2.started) {
    G2.secondsLeft = gs.secondsLeft ?? 210;
    startG2Timer(G2.secondsLeft);
    showCurrentConcept(state);
  }
  // If not started, the start overlay is rendered by renderChapter2; no bubbles to populate
}

