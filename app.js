// Theme helpers
function initTheme() {
  if (localStorage.getItem(THEME_KEY) === 'light') {
    document.body.classList.add('light-mode');
  }
}

function toggleTheme() {
  document.body.classList.toggle('light-mode');
  const isLight = document.body.classList.contains('light-mode');
  localStorage.setItem(THEME_KEY, isLight ? 'light' : 'dark');
  const icon = document.getElementById('theme-icon');
  if (icon) icon.textContent = isLight ? '🌙' : '☀️';
}

// Confetti burst
function launchConfetti(originEl) {
  const colors = ['#8b5cf6','#22d3ee','#10b981','#f59e0b','#ec4899','#f43f5e','#a78bfa','#67e8f9'];
  const rect = originEl ? originEl.getBoundingClientRect() : { left: window.innerWidth/2, top: window.innerHeight/2, width: 0, height: 0 };
  const cx = rect.left + rect.width / 2;
  const cy = rect.top  + rect.height / 2;
  for (let i = 0; i < 32; i++) {
    const el = document.createElement('div');
    el.className = 'confetti-particle';
    const color = colors[Math.floor(Math.random() * colors.length)];
    el.style.cssText = `left:${cx}px;top:${cy}px;background:${color};border-radius:${Math.random()>0.5?'50%':'3px'};`;
    document.body.appendChild(el);
    const angle = (Math.random() * Math.PI * 2);
    const speed = 80 + Math.random() * 130;
    const dx = Math.cos(angle) * speed;
    const dy = Math.sin(angle) * speed - 70;
    el.animate(
      [{ transform:'translate(0,0) rotate(0deg) scale(1)', opacity:1 },
       { transform:`translate(${dx}px,${dy+100}px) rotate(${Math.random()*720}deg) scale(0)`, opacity:0 }],
      { duration: 700 + Math.random()*500, easing:'cubic-bezier(0.4,0,0.2,1)' }
    ).onfinish = () => el.remove();
  }
}

